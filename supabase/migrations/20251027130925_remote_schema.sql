CREATE OR REPLACE FUNCTION public.uuid_base62(prefix text DEFAULT ''::text)
 RETURNS text
 LANGUAGE plpgsql
AS $function$
DECLARE
    raw UUID := uuid_generate_v4();
    b64 text;
BEGIN
    b64 := encode(uuid_send(raw), 'base64');
    b64 := regexp_replace(b64, '[^a-zA-Z0-9]', '', 'g');
    RETURN prefix || b64;
END;
$function$
;


